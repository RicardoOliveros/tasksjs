// Escribe un programa que con una variable número del 1 al 7 y muestre por consola el día de la semana correspondiente.

let dia = prompt('Enter a number to know the day of the week from 1 to 7')

if(dia == 1){
    alert('Monday')
}
else if(dia == 2){
    alert('Tuesday')
}
else if(dia == 3){
    alert('Wednesday')
}
else if(dia == 4){
    alert('Thrusday')
}
else if(dia == 5){
    alert('Friday')
}
else if(dia == 6){
    alert('Saturday')
}
else if(dia == 7){
    alert('Sunday')
}
else{
    alert('404 ERROR')
}